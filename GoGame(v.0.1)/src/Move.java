public class Move {
    private int x, y;

    Move(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public void setX(int xin) {
        x = xin;
    }

    public void setY(int yin) {
        y = yin;
    }
}
